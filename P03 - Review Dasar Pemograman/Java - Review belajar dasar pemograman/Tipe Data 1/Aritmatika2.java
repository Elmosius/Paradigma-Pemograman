public class Aritmatika2 {
    public static void main(String[] args) {
        int a,b;

        a = 7;
        b = 3;

        System.out.println("\na = " + a);
        System.out.println("b = " + b);
        System.out.println("a++ = " + (a++));
        System.out.println("++a = " + (++a));
        System.out.println("a-- = " + (a--));
        System.out.println("--a = " + (--a));
    }
}
