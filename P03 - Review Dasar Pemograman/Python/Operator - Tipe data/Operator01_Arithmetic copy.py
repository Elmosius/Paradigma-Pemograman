# Opeator Aritmetik

def main():
    a = 7
    b = 2
    
    print(a, " + ", b, " = ", a+b)
    print(a, " - ", b, " = ", a-b)
    print(a, " * ", b, " = ", a*b)
    print(a, " / ", b, " = ", a/b)
    print(a, " % ", b, " = ", a%b)
    print(a, " ** ", b, " = ", a**b)
    print(a, " // ", b, " = ", a//b)
    
    
if __name__ == "__main__":
    main()            