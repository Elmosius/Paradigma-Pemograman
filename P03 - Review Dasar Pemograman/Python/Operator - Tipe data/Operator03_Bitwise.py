# Opeator bitwise

def main():
    a = 3
    b = 5
    print("a = ",a, " = ", bin(a))
    print("b = ",b, " = ", bin(b))
    print("a & b : ", a & b)
    print("a | b : ", a | b)
    print("a ^ b : ", a ^ b)
    print("b << 2 : ", b << 2)
    print("b >> 2 : ",b >> 2)
    print("~b : ", ~b)
    
if __name__ == "__main__":
    main()            